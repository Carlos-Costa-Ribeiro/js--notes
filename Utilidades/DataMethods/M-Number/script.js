// A função "n.toString()", converte uma variavel que seja um numero para uma string.
let n = 25;
let nr = n.toString() + 300;
console.log (nr);

// A função "n.toFixed(2)", essa função preserva os numeros após o "." ou ","
//a quantidade de numeros a seres preservados é passado pelo parâmetro.
let x = 10.45475;
let xr = x.toFixed(2)
console.log (xr)

// A função "parseInt(y)" tranforma um número real em inteiro.
let y = "30";
let yr = parseInt(y) + 30;
console.log (yr)

// A função "parseFloat(h)" preserva os numeros apos o ".".
let h = "30.4575";
let hr = parseFloat(h) + 30;
console.log (hr)

